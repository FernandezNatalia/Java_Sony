package entidades;

public class Plan {
	
	private int id;
	private int id_os;
	private String nomplan;
	private String nom_os;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getId_os() {
		return id_os;
	}
	public void setId_os(int id_os) {
		this.id_os = id_os;
	}
	public String getNomplan() {
		return nomplan;
	}
	public void setNomplan(String nomplan) {
		this.nomplan = nomplan;
	}
	public String getNom_os() {
		return nom_os;
	}
	public void setNom_os(String nom_os) {
		this.nom_os = nom_os;
	}

}
